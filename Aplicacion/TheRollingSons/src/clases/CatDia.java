package clases;
public class CatDia {
    private int idDia;
    private String dia;

    /**
     * @return the idDia
     */
    public int getIdDia() {
        return idDia;
    }

    /**
     * @param idDia the idDia to set
     */
    public void setIdDia(int idDia) {
        this.idDia = idDia;
    }

    /**
     * @return the dia
     */
    public String getDia() {
        return dia;
    }

    /**
     * @param dia the dia to set
     */
    public void setDia(String dia) {
        this.dia = dia;
    }
    
}
