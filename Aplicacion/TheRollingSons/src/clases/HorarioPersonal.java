package clases;
public class HorarioPersonal {
    private int idHorarioPersonal;
    private Horario horario;
    private Personal personal;
    
    public int getIdHorarioPersonal() {
        return idHorarioPersonal;
    }
    public void setIdHorarioPersonal(int idHorarioPersonal) {
        this.idHorarioPersonal = idHorarioPersonal;
    }
    public Horario getHorario() {
        return horario;
    }
    public void setHorario(Horario horario) {
        this.horario = horario;
    }
    public Personal getPersonal() {
        return personal;
    }
    public void setPersonal(Personal personal) {
        this.personal = personal;
    }
    
}
